    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed

        String ID = id.getText();
        String pay = "Not Paid";
        if(notpaid.isSelected()){
            pay = "Not Paid";
        }else if(paid.isSelected()){
            pay = "Paid";
        }
        

        if(verifText()){
            payment s = new payment();
            s.insertUpdateDeletePayment('u', ID, pay);
            //            MainForm.jLabel_instructor.setText("Subject = "+Integer.toString( MyFunction.countData("subject")));
            mngpayment.jTable.setModel(new DefaultTableModel(null, new Object[]{"ID","PAY"}));
            s.fillPaymentJtable(mngpayment.jTable, "");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

