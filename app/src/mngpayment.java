    public boolean verifText(){
        if(id.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "ID field is empty!!!");
            return false;
        }
        else{
            return true;
        }
    }
    
    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked

        int rowIndex = jTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable.getModel();

        id.setText(model.getValueAt(rowIndex, 0).toString());
        
        if(model.getValueAt(rowIndex, 1).toString().equals("Paid"))
        {
            paid.setSelected(true);
            notpaid.setSelected(false);
        }
        else if(model.getValueAt(rowIndex, 1).toString().equals("Not Paid"))
        {
            paid.setSelected(false);
            notpaid.setSelected(true);
        }
        
    }//GEN-LAST:event_jTableMouseClicked

