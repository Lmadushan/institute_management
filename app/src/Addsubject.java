    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked

        int rowIndex = jTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable.getModel();

        if(model.getValueAt(rowIndex, 3).toString().equals("1"))
        {
            first.setSelected(true);
            second.setSelected(false);
            
        }
        else if(model.getValueAt(rowIndex, 3).toString().equals("2"))
        {
            first.setSelected(false);
            second.setSelected(true);
            
        }
        

       
        code.setText(model.getValueAt(rowIndex, 0).toString());
        name.setText(model.getValueAt(rowIndex, 1).toString());
        teacher.setText(model.getValueAt(rowIndex, 2).toString());
        fees.setText(model.getValueAt(rowIndex, 3).toString());
        credit.setText(model.getValueAt(rowIndex, 4).toString());
        course.setText(model.getValueAt(rowIndex, 6).toString());
        location.setText(model.getValueAt(rowIndex, 7).toString());
        
    }//GEN-LAST:event_jTableMouseClicked

