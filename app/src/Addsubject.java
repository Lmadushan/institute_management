    public boolean verifText(){
        if(code.getText().equals("") || name.getText().equals("") || teacher.getText().equals("") || fees.getText().equals("") || credit.getText().equals("") || course.getText().equals("") || location.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Some fields are empty!!!");
            return false;
        }
        else{
            return true;
        }
    }
    
    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed

        String Code = code.getText();
        String Name = name.getText();
        String Teacher = teacher.getText();
        String Fees = fees.getText();
        String Credit = credit.getText();
        String Course = course.getText();
        String Location = location.getText();

        
        String semester = "";
        if(first.isSelected()){
            semester = "1";

        }else if(second.isSelected()){
            semester = "2";
        }

        //        String acayear = pgstdacayear.getText();

        if(verifText()){
            subject sub = new subject();
            sub.insertUpdateDeletesubject('u', Code, Name, Teacher,Fees,Credit,semester,Course,Location);
            //MainForm.jLabel_instructor.setText("Subject = "+Integer.toString( MyFunction.countData("subjects")));
            Addsubject.jTable.setModel(new DefaultTableModel(null, new Object[]{"Code","Name","Teacher","Fees","Credit","Semester","Course","Location"}));
            sub.fillSubjectJtable(Addsubject.jTable, "");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

